    private void fileComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileComboBoxActionPerformed
            int index = fileComboBox.getSelectedIndex();
            if (index >= 0 && frameInitialised){
            load(files[index]);
            }
    }
    public void load(File f){
        try
        {
            FileInputStream fis = new FileInputStream(f);
            ObjectInputStream ois = new ObjectInputStream(fis);
            BellumDeorum loadedGame = (BellumDeorum) ois.readObject();
            ois.close();
            loadedGame.newGame = false;
            loadedGame.start();
            this.dispose();
        }
        catch (Exception ex)
        {
            System.out.println("Exception thrown during test: " + ex.toString());
        }
    
    }//GEN-LAST:event_fileComboBoxActionPerformed

