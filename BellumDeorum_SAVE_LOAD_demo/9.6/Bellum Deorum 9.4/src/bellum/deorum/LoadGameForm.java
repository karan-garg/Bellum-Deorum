    public File[] genFileList(){
        File dir = new File("src//user_data");
        File[] filesList = dir.listFiles();
        if (filesList == null){
            filesList = new File[1];
        }
        return filesList;
    }
    
    public void setComboBoxOptions(){
        for (int i = 0; i < files.length; i++){
            if (files[i] != null){
                Date lastModified = new Date(files[i].lastModified()); 
                SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm");  
                String formattedDateString = formatter.format(lastModified); 
                fileComboBox.addItem(files[i].getName() + " " + formattedDateString);
            }
        }
    }
